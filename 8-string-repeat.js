/* Write a function called repeatStr which repeats the given string string exactly n times.

repeatStr(6, "I") // "IIIIII"
repeatStr(5, "Hello") // "HelloHelloHelloHelloHello"
*/


// MY SOLUTION


  
  
/* TOP CODE WARS SOLUTION
 
function repeatStr (n, s) {
  return s.repeat(n);
}

*/